
// ─────────────────────────────────────────────────────────────────────────────
// bot.js — ініціалізація бота та реєстрація всіх обробників
//
// Тут живе "кістяк" бота: middleware, команди, реакції на текст і callback.
// Файл експортує готовий екземпляр bot — index.js просто його запускає.
// ─────────────────────────────────────────────────────────────────────────────
 
// Telegraf — Node.js-фреймворк для Telegram Bot API.
// Ми деструктуруємо лише те, що потрібно: клас Telegraf для створення бота.
// (У keyboards/*.js будемо імпортувати Markup окремо — щоб не тягнути зайве сюди)
const { Telegraf } = require('telegraf');
 
// Токен бота з config.js (той, у свою чергу, читає його з process.env.BOT_TOKEN).
// Розділяємо config і bot — щоб не змішувати "де зберігаються дані" і "як працює бот".
const { BOT_TOKEN } = require('./config/config');
 
// Handlers — функції, які обробляють конкретні типи подій.
// Кожен handler — окремий файл: легше знаходити, тестувати, замінювати.
const startHandler    = require('./handlers/startHandler');
const messageHandler  = require('./handlers/messageHandler');
 
// Локалі потрібні тут для обробки callback "Подарунок".
// Коли користувач натискає кнопку — ми вже не знаємо його "поточну мову"
// (Telegram не зберігає стан між апдейтами), тому мова закодована
// прямо в callback_data: 'gift:uk' або 'gift:ru'.
const uk = require('./locales/uk');
const ru = require('./locales/ru');
 
// canProceed — утиліта захисту від повторних кліків.
// Використовуємо тут для кнопки "Подарунок", і в messageHandler для мовної кнопки.
const { canProceed } = require('./utils/throttle');
 
// Кулдаун для кнопки "Подарунок" — коротший, ніж для вибору мови,
// бо обробка складається лише з одного ctx.reply().
const GIFT_COOLDOWN_MS = 3_000;
 
// ─────────────────────────────────────────────────────────────────────────────
// ВАЛІДАЦІЯ ТОКЕНА
// ─────────────────────────────────────────────────────────────────────────────
//
// Telegraf не кидає помилку відразу при new Telegraf(undefined) —
// він впаде пізніше, при першому запиті до API, з незрозумілим повідомленням.
// Краще зловити проблему тут, з чітким поясненням.
//
if (!BOT_TOKEN) {
  throw new Error(
    'BOT_TOKEN не знайдений. Переконайтеся, що файл .env існує і містить BOT_TOKEN=...'
  );
}
 
// ─────────────────────────────────────────────────────────────────────────────
// СТВОРЕННЯ ЕКЗЕМПЛЯРА БОТА
// ─────────────────────────────────────────────────────────────────────────────
//
// new Telegraf(token) створює об'єкт бота, але ще НЕ підключається до Telegram.
// Підключення відбудеться пізніше — коли index.js викличе bot.launch().
//
const bot = new Telegraf(BOT_TOKEN);
 
// ─────────────────────────────────────────────────────────────────────────────
// MIDDLEWARE
// ─────────────────────────────────────────────────────────────────────────────
//
// Middleware — функція, яка виконується ДО основного handler для КОЖНОГО апдейту.
// Механізм схожий на Express.js: bot.use((ctx, next) => { ... next() ... }).
//
// ctx (context) — головний об'єкт Telegraf. Він містить:
//   ctx.message       — об'єкт повідомлення (якщо апдейт — повідомлення)
//   ctx.callbackQuery — дані callback (якщо натиснута inline-кнопка)
//   ctx.from          — інформація про користувача
//   ctx.chat          — інформація про чат
//   ctx.reply()       — shortcut для sendMessage в поточний чат
//   ... та десятки інших методів
//
// next() — передає управління наступному middleware або handler.
// Якщо не викликати next() — ланцюжок обривається і handler не спрацює.
//
bot.use(async (ctx, next) => {
  const start = Date.now();
 
  // Виконуємо наступний middleware/handler і чекаємо його завершення
  await next();
 
  const ms = Date.now() - start;
 
  // Логуємо тип апдейту та час обробки — зручно при налагодженні
  // ctx.updateType: 'message', 'callback_query', 'inline_query', тощо
  console.log(`[${new Date().toISOString()}] ${ctx.updateType} — ${ms}ms`);
});
 
// ─────────────────────────────────────────────────────────────────────────────
// КОМАНДИ
// ─────────────────────────────────────────────────────────────────────────────
//
// bot.start() — слухає команду /start.
// Це скорочення для bot.command('start', handler).
// Спрацьовує також при натисканні кнопки "Start" у новому чаті з ботом.
//
bot.start(startHandler);
 
// ─────────────────────────────────────────────────────────────────────────────
// РЕАКЦІЯ НА ТЕКСТ КЛАВІАТУРИ
// ─────────────────────────────────────────────────────────────────────────────
//
// bot.hears() слухає конкретні текстові повідомлення (точний збіг або RegExp).
// Перший аргумент — масив рядків або RegExp — шаблон пошуку.
// Якщо текст повідомлення збігається з будь-яким елементом масиву —
// викликається messageHandler.
//
// ❓ Чому не bot.on('text', messageHandler)?
//   bot.on('text') спрацьовував би НА БУДЬ-ЯКИЙ текст, включаючи випадкові
//   повідомлення користувача. Тоді фільтрацію довелось би робити всередині
//   handler вручну. bot.hears() робить це декларативно — чистіший код.
//
// Захист від подвійних кліків реалізовано всередині messageHandler
// через canProceed() з utils/throttle.js.
//
bot.hears(['🇺🇦 Українська', '🇷🇺 Русский'], messageHandler);
 
// ─────────────────────────────────────────────────────────────────────────────
// CALLBACK-ОБРОБНИК ДЛЯ КНОПКИ "ПОДАРУНОК"
// ─────────────────────────────────────────────────────────────────────────────
//
// bot.action() слухає callback_query — події натискання inline-кнопок.
// Перший аргумент — рядок або RegExp для зіставлення з callback_data кнопки.
//
// Ми використовуємо RegExp /gift:(uk|ru)/, щоб:
//   1. Відловити обидва варіанти ('gift:uk' і 'gift:ru') одним обробником
//   2. Витягнути код мови через ctx.match[1] (перша capture group)
//
// ctx.match — результат .exec(callback_data), тобто:
//   ctx.match[0] === 'gift:uk'   (повний збіг)
//   ctx.match[1] === 'uk'        (перша група — те, що в дужках)
//
bot.action(/gift:(uk|ru)/, async (ctx) => {
 
  // answerCbQuery() — ОБОВ'ЯЗКОВО ПЕРШИМ, ще до перевірки кулдауну.
  //
  // Причина: Telegram показує спіннер на кнопці з моменту натискання
  // і чекає відповіді до 30 секунд. Якщо поставити answerCbQuery після
  // canProceed і вийти через return — спіннер залишиться крутитись.
  //
  // Порядок: спочатку прибираємо спіннер, потім вирішуємо що робити далі.
  //
  await ctx.answerCbQuery();
 
  // Перевіряємо кулдаун після answerCbQuery.
  // Якщо користувач в кулдауні — спіннер вже прибрано, тихо виходимо.
  //
  if (!canProceed(ctx.from.id, 'gift', GIFT_COOLDOWN_MS)) return;
 
  const lang   = ctx.match[1];                    // 'uk' або 'ru'
  const locale = lang === 'uk' ? uk : ru;         // вибираємо потрібну локаль
 
  await ctx.reply(locale.giftText);
});
 
// ─────────────────────────────────────────────────────────────────────────────
// ГЛОБАЛЬНИЙ ОБРОБНИК ПОМИЛОК
// ─────────────────────────────────────────────────────────────────────────────
//
// bot.catch() перехоплює будь-яку необроблену помилку у handlers або middleware.
// БЕЗ цього — непередбачена помилка у handler зупинить весь процес бота.
//
// err — об'єкт помилки
// ctx — контекст апдейту, під час якого сталась помилка (може бути undefined)
//
bot.catch((err, ctx) => {
  console.error(`[ПОМИЛКА] під час обробки ${ctx?.updateType ?? 'невідомий апдейт'}:`);
  console.error(err);
 
  // Намагаємось повідомити користувача, що щось пішло не так.
  // Загортаємо в try/catch — щоб повідомлення про помилку саме не впало.
  ctx?.reply('Сталася помилка. Спробуйте ще раз або надішліть /start').catch(() => {});
});
 
// ─────────────────────────────────────────────────────────────────────────────
// ЕКСПОРТ
// ─────────────────────────────────────────────────────────────────────────────
//
// Експортуємо готовий, налаштований екземпляр бота.
// index.js отримає його і викличе bot.launch() для запуску.
//
module.exports = bot;
 