// ─────────────────────────────────────────────────────────────────────────────
// src/db/prisma.js — єдиний екземпляр Prisma Client
//
// Навіщо окремий файл, а не new PrismaClient() у кожному модулі?
//
// PrismaClient відкриває пул підключень до БД при створенні.
// Якщо створювати новий екземпляр у кожному файлі — отримаємо
// десятки відкритих пулів, що швидко вичерпає ліміт підключень PostgreSQL
// (за замовчуванням 100).
//
// Патерн Singleton: один екземпляр на весь процес Node.js.
// Node кешує require() — повторний require('./db/prisma') у будь-якому
// файлі поверне той самий об'єкт, що вже існує в пам'яті.
// ─────────────────────────────────────────────────────────────────────────────

const { PrismaClient } = require('@prisma/client');

// log: налаштовує що Prisma виводить у консоль.
// 'query'  — кожен SQL-запит (зручно при налагодженні, вимкніть на продакшні)
// 'error'  — помилки БД (залиште завжди)
// 'warn'   — попередження (залиште завжди)
//
const prisma = new PrismaClient({
  log: process.env.NODE_ENV === 'development'
    ? ['query', 'error', 'warn']
    : ['error', 'warn'],
});

module.exports = { prisma };
