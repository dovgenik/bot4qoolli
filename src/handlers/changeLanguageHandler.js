// ─────────────────────────────────────────────────────────────────────────────
// handlers/changeLanguageHandler.js — обробник команди /language
//
// Дозволяє юзеру змінити мову у будь-який момент.
// Особливо важливо для юзерів, яких автовизначило на одну мову,
// але вони хочуть іншу — або для юзерів з непідтримуваною мовою Telegram.
// ─────────────────────────────────────────────────────────────────────────────

const { languageKb } = require('../keyboards/languageKb');

module.exports = async (ctx) => {
  // Надсилаємо нейтральний prompt — не знаємо мову юзера в цей момент,
  // тому пишемо на всіх підтримуваних мовах одразу.
  await ctx.reply(
    'Оберіть мову / Выберите язык:',
    languageKb
  );
};