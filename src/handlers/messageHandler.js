// ─────────────────────────────────────────────────────────────────────────────
// handlers/messageHandler.js — обробник вибору мови
//
// Викликається з bot.js коли користувач натискає кнопку мови:
//   bot.hears(['🇺🇦 Українська', '🇷🇺 Русский'], messageHandler)
//
// Послідовно надсилає три речі: відео → текст → inline-меню.
// ─────────────────────────────────────────────────────────────────────────────
 
const uk = require('../locales/uk');
const ru = require('../locales/ru');
const { mainMenuKb } = require('../keyboards/mainMenuKb');
const { canProceed } = require('../utils/throttle');
 
// ─────────────────────────────────────────────────────────────────────────────
// КУЛДАУН
// ─────────────────────────────────────────────────────────────────────────────
//
// 8 секунд — з запасом покриває повний час надсилання трьох повідомлень
// (sendChatAction + sendVideo + два reply), навіть при повільній мережі.
//
// Якщо користувач клікне ще раз під час або одразу після відправки —
// canProceed поверне false і handler просто завершиться без жодних дій.
//
// Числовий роздільник 8_000 (ES2021) — лише для читабельності,
// для Node.js це звичайне число 8000.
//
const COOLDOWN_MS = 8_000;
 
// ─────────────────────────────────────────────────────────────────────────────
// LOOKUP-ТАБЛИЦЯ ЛОКАЛЕЙ
// ─────────────────────────────────────────────────────────────────────────────
//
// Замість if/else ланцюжка використовуємо об'єкт як словник (lookup table):
//
//   ❌ if/else — працює, але не масштабується:
//      if (text === '🇺🇦 Українська') { locale = uk }
//      else if (text === '🇷🇺 Русский') { locale = ru }
//
//   ✅ lookup table — O(1), легко розширювати:
//      const LOCALES = { '🇺🇦 Українська': uk, '🇷🇺 Русский': ru }
//      const locale = LOCALES[text]  // одна операція
//
// Оголошуємо ПОЗА функцією — об'єкт будується один раз при завантаженні
// модуля, а не при кожному виклику handler-а.
//
const LOCALES = {
  '🇺🇦 Українська': uk,
  '🇷🇺 Русский':    ru,
};
 
// ─────────────────────────────────────────────────────────────────────────────
 
module.exports = async (ctx) => {
 
  // ── Захист від повторних кліків ───────────────────────────────────────────
  //
  // Перевіряємо ПЕРШИМ РЯДКОМ — до будь-якої іншої логіки.
  // Якщо кулдаун ще не минув: мовчки виходимо, нічого не надсилаємо.
  //
  // ctx.from.id — унікальний Telegram ID користувача.
  // 'lang_select' — ідентифікатор дії: дозволяє мати окремий кулдаун
  // для цього handler-а, незалежно від кулдауну кнопки "Подарунок" у bot.js.
  //
  if (!canProceed(ctx.from.id, 'lang_select', COOLDOWN_MS)) return;
 
  // ctx.message.text — текст повідомлення, яке надіслав користувач.
  // Оскільки handler викликається через bot.hears(), сюди потрапляє
  // лише '🇺🇦 Українська' або '🇷🇺 Русский' — інші тексти bot.hears() відсіює.
  //
  const locale = LOCALES[ctx.message.text];
 
  // Guard clause — захист на випадок непередбаченого тексту.
  // bot.hears() теоретично гарантує збіг, але явна перевірка — хороша звичка:
  // захищає від майбутніх рефакторингів (якщо хтось змінить bot.hears без LOCALES).
  //
  if (!locale) return;
 
  // ── Крок 1: "друкує..." ────────────────────────────────────────────────────
  //
  // sendChatAction показує користувачу статус у верхній частині чату
  // ("надсилає відео...") поки бот готує відповідь.
  // Це покращує UX: без нього здається, що бот "завис".
  //
  // Доступні типи: 'typing', 'upload_photo', 'upload_video',
  //               'upload_document', 'find_location' тощо.
  // Статус автоматично зникає через 5 секунд або коли надійде наступне повідомлення.
  //
  await ctx.sendChatAction('upload_video');
 
  // ── Крок 2: Відео ─────────────────────────────────────────────────────────
  //
  // ctx.sendVideo(fileId, extra?) надсилає відео в поточний чат.
  //
  // Перший аргумент — file_id або об'єкт { url: '...' }:
  //   ctx.sendVideo(locale.videoFileId)              // file_id (швидко)
  //   ctx.sendVideo({ url: 'https://..../v.mp4' })   // URL (повільніше)
  //
  // Другий аргумент (extra) — опціональні параметри, наприклад підпис:
  //   ctx.sendVideo(locale.videoFileId, { caption: 'Опис відео' })
  //
  await ctx.sendVideo(locale.videoFileId);
 
  // ── Крок 3: Привітальний текст ────────────────────────────────────────────
  //
  // Надсилаємо окремим повідомленням після відео.
  // Telegram не гарантує порядок доставки, якщо надсилати без await:
  // текст теоретично може прийти ПЕРЕД відео.
  // Sequential await вирішує це: кожен наступний await стартує лише
  // після підтвердження попереднього від Telegram API.
  //
  await ctx.reply(locale.welcomeText);
 
  // ── Крок 4: Inline-меню ───────────────────────────────────────────────────
  //
  // ctx.reply() з двома аргументами: текст + об'єкт з клавіатурою.
  // mainMenuKb(locale) будує Markup.inlineKeyboard з правильними підписами
  // і callback_data для поточної мови.
  //
  // Telegram вимагає, щоб inline keyboard був прикріплений до повідомлення —
  // не можна надіслати клавіатуру "саму по собі", тому locale.menuText обов'язковий.
  //
  await ctx.reply(locale.menuText, mainMenuKb(locale));
 
};