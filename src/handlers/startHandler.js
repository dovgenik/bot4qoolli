// ─────────────────────────────────────────────────────────────────────────────
// handlers/startHandler.js — обробник команди /start
// ─────────────────────────────────────────────────────────────────────────────
 
const { languageKb } = require('../keyboards/languageKb');
 
// ─────────────────────────────────────────────────────────────────────────────
// СИГНАТУРА HANDLER-А
// ─────────────────────────────────────────────────────────────────────────────
//
// Кожен handler у Telegraf — це async-функція, яка отримує один аргумент: ctx.
//
// ctx (Context) — об'єкт, який Telegraf будує для кожного вхідного апдейту.
// Він об'єднує в собі:
//   - дані апдейту (ctx.message, ctx.from, ctx.chat...)
//   - методи для відповіді (ctx.reply, ctx.sendVideo, ctx.answerCbQuery...)
//   - доступ до низькорівневого API (ctx.telegram.sendMessage(chatId, text))
//
// Завжди async — надсилання повідомлень є мережевими запитами до Telegram API,
// тому вони асинхронні. await гарантує, що наступний рядок виконається
// лише після того, як Telegram підтвердить отримання.
//
module.exports = async (ctx) => {
 
  // ctx.reply(text, extra?) — найпоширеніший метод.
  // Це shortcut для ctx.telegram.sendMessage(ctx.chat.id, text, extra).
  // "Відповідає" в той самий чат, звідки прийшла команда.
  //
  // Другий аргумент — об'єкт extra з додатковими параметрами.
  // Тут ми передаємо languageKb — це і є об'єкт Reply keyboard,
  // який Telegram приєднає до повідомлення і покаже замість стандартної клавіатури.
  //
  // Текст "Оберіть мову / Выберите язык:" — нейтральний: двомовний,
  // бо ми ще не знаємо, яку мову обере користувач.
  //
  await ctx.reply('Оберіть мову / Выберите язык:', languageKb);
 
};