// ─────────────────────────────────────────────────────────────────────────────
// index.js — точка входу застосунку
//
// Цей файл відповідає лише за ЗАПУСК бота.
// Вся логіка (handlers, keyboards, locales) живе в інших модулях.
// Такий поділ спрощує тестування та супровід коду.
// ─────────────────────────────────────────────────────────────────────────────

// dotenv читає файл .env і додає всі змінні до process.env.
// ВАЖЛИВО: цей рядок має бути ПЕРШИМ — ще до імпорту bot.js,
// бо bot.js одразу звертається до process.env.BOT_TOKEN при ініціалізації.
// Якщо викликати config() пізніше — BOT_TOKEN буде undefined.
require('dotenv').config();

// Імпортуємо готовий екземпляр бота з bot.js.
// Там вже зареєстровані всі команди (/start), handlers та keyboards.
const bot = require('./bot');

// ─────────────────────────────────────────────────────────────────────────────
// ЗАПУСК БОТА — Long Polling
// ─────────────────────────────────────────────────────────────────────────────
//
// bot.launch() запускає механізм long polling:
//   бот постійно надсилає запити до Telegram API ("є нові апдейти?"),
//   Telegram тримає з'єднання відкритим до 25 секунд і відповідає,
//   коли надходить нове повідомлення.
//
// Це зручно для розробки — не потрібен публічний HTTPS-сервер.
//
// ❗ Для продакшену краще використовувати WEBHOOKS:
//   bot.createWebhook({ domain: 'https://your-domain.com' })
//   Тоді Telegram сам надсилає апдейти на ваш сервер — без постійних запитів.
//
bot.launch()
  .then(() => {
    console.log('✅ Бот успішно запущений (long polling)');
    console.log(`📌 Режим: ${process.env.NODE_ENV || 'development'}`);
  })
  .catch((err) => {
    // Найпоширеніші причини помилки тут:
    //   - неправильний або відсутній BOT_TOKEN у .env
    //   - немає інтернет-з'єднання
    //   - бот вже запущений в іншому процесі (конфлікт polling)
    console.error('❌ Не вдалося запустити бота:', err.message);
    process.exit(1); // завершуємо процес із кодом помилки
  });

// ─────────────────────────────────────────────────────────────────────────────
// GRACEFUL SHUTDOWN — коректне завершення роботи
// ─────────────────────────────────────────────────────────────────────────────
//
// Операційна система надсилає сигнали процесу, коли його треба зупинити:
//   SIGINT  — Ctrl+C у терміналі під час розробки
//   SIGTERM — штатна зупинка від системи (Docker stop, PM2 reload, деплой)
//
// bot.stop() коректно завершує polling:
//   - чекає, доки обробиться поточний апдейт
//   - закриває HTTP-з'єднання з Telegram
//   - звільняє ресурси
//
// process.once (не .on) — важливо: обробник спрацює лише ОДИН раз.
// Якщо використати .on і сигнал надійде двічі — bot.stop() викличеться
// повторно, що може спричинити помилку.
//
process.once('SIGINT', () => {
  console.log('\n⏹  Отримано SIGINT. Зупиняємо бота...');
  bot.stop('SIGINT');
});

process.once('SIGTERM', () => {
  console.log('\n⏹  Отримано SIGTERM. Зупиняємо бота...');
  bot.stop('SIGTERM');
});