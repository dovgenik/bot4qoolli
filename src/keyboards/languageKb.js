// ─────────────────────────────────────────────────────────────────────────────
// keyboards/languageKb.js — клавіатура вибору мови
// ─────────────────────────────────────────────────────────────────────────────
 
// Markup — утиліта Telegraf для побудови клавіатур.
// Імпортуємо саме тут, а не в bot.js: кожен модуль відповідає за свою клавіатуру.
const { Markup } = require('telegraf');
 
// ─────────────────────────────────────────────────────────────────────────────
// ТИПИ КЛАВІАТУР У TELEGRAM
// ─────────────────────────────────────────────────────────────────────────────
//
// Telegram має два принципово різних типи клавіатур:
//
// 1. Reply keyboard (ReplyKeyboardMarkup) ← використовуємо тут
//    - З'являється ЗАМІСТЬ стандартної клавіатури телефону
//    - Натискання = звичайне текстове повідомлення від користувача
//    - Бот отримує ctx.message.text з текстом кнопки
//    - Видно всім у чаті (не прихована)
//    - Створюється через: Markup.keyboard()
//
// 2. Inline keyboard (InlineKeyboardMarkup) ← у mainMenuKb.js
//    - Кнопки прикріплені до конкретного повідомлення бота
//    - Натискання НЕ надсилає текст — генерує callback_query або відкриває URL
//    - Бот отримує ctx.callbackQuery.data
//    - Створюється через: Markup.inlineKeyboard()
//
// ─────────────────────────────────────────────────────────────────────────────
// СТРУКТУРА Markup.keyboard()
// ─────────────────────────────────────────────────────────────────────────────
//
// Приймає масив рядків (rows), кожен рядок — масив кнопок:
//
//   Markup.keyboard([
//     ['Кнопка 1', 'Кнопка 2'],   // перший рядок: дві кнопки поруч
//     ['Кнопка 3'],                // другий рядок: одна кнопка
//   ])
//
// У нас обидві мовні кнопки — в одному рядку, поруч:
//   ['🇺🇦 Українська', '🇷🇺 Русский']
//
const languageKb = Markup.keyboard([
  ['🇺🇦 Українська', '🇷🇺 Русский'],
])
  // .resize() — підганяє висоту клавіатури під кількість кнопок.
  // БЕЗ resize() клавіатура займає третину екрану незалежно від кількості рядків.
  // Завжди рекомендується використовувати.
  .resize()
 
  // .oneTime() — клавіатура зникає після того, як користувач натисне будь-яку кнопку.
  // Без цього вона залишається на екрані постійно — доки бот не приховає її явно.
  // Для одноразового вибору мови — це правильна поведінка.
  .oneTime();
 
// ─────────────────────────────────────────────────────────────────────────────
// Чому константа, а не функція?
//
// languageKb не залежить від жодних зовнішніх даних (не приймає locale, config тощо).
// Клавіатура однакова для всіх користувачів — будуємо її один раз при завантаженні
// модуля і перевикористовуємо. Це ефективніше, ніж створювати новий об'єкт
// при кожному виклику /start.
//
// Порівняйте з mainMenuKb.js — там функція, бо клавіатура залежить від locale.
// ─────────────────────────────────────────────────────────────────────────────
 
module.exports = { languageKb };