// ─────────────────────────────────────────────────────────────────────────────
// keyboards/mainMenuKb.js — inline-меню з чотирьох кнопок
// ─────────────────────────────────────────────────────────────────────────────
 
const { Markup } = require('telegraf');
 
// URL-адреси беремо з config — клавіатура не повинна знати, де вони зберігаються.
const { SITE_URL, CHANNEL_URL, CONSULT_URL } = require('../config/config');
 
// ─────────────────────────────────────────────────────────────────────────────
// ТИПИ INLINE-КНОПОК
// ─────────────────────────────────────────────────────────────────────────────
//
// Telegraf надає кілька типів кнопок для inlineKeyboard.
// Ми використовуємо два:
//
// Markup.button.url(text, url)
//   - При натисканні відкриває посилання у браузері або Telegram
//   - НЕ генерує callback — бот не отримує жодної події
//   - Ідеально для зовнішніх сайтів і Telegram-каналів
//
// Markup.button.callback(text, data)
//   - При натисканні надсилає боту callback_query з полем data
//   - Бот отримує подію і може відповісти текстом, змінити повідомлення тощо
//   - data — рядок до 64 байт (❗ обмеження Telegram API)
//   - Обробляється через bot.action() у bot.js
//
// Інші типи (не використовуємо, але корисно знати):
//   Markup.button.webApp(text, url)    — відкриває Telegram Web App
//   Markup.button.switchToChat(text)   — forwarding у inline-режимі
//   Markup.button.pay(text)            — кнопка оплати (Telegram Payments)
//
// ─────────────────────────────────────────────────────────────────────────────
// СТРУКТУРА LAYOUT — масив рядків
// ─────────────────────────────────────────────────────────────────────────────
//
// Markup.inlineKeyboard приймає масив рядків (rows), кожен рядок — масив кнопок.
// Telegram відобразить їх відповідно до цієї сітки:
//
//   [[кнопка A, кнопка B],   // рядок 1: дві кнопки поруч
//    [кнопка C]]             // рядок 2: одна кнопка на весь рядок
//
// У нас кожна кнопка — в окремому рядку [[btn1], [btn2], [btn3], [btn4]].
// Це стандарт для меню дій: кнопки не конкурують між собою за ширину
// і підписи читаються повністю без скорочень.
//
// ─────────────────────────────────────────────────────────────────────────────
// Чому функція, а не константа?
// ─────────────────────────────────────────────────────────────────────────────
//
// Клавіатура залежить від locale: підписи кнопок — різні для UA і RU,
// callback_data для "Подарунку" містить locale.lang ('gift:uk' або 'gift:ru').
// Тому mainMenuKb — функція, яка приймає locale і щоразу будує новий об'єкт.
//
// Порівняйте з languageKb.js — там константа, бо клавіатура незмінна.
//
const mainMenuKb = (locale) =>
  Markup.inlineKeyboard([
 
    // Кнопка 1: "Зареєструватися" — відкриває сайт реєстрації
    // Markup.button.url(підпис, посилання)
    [Markup.button.url(locale.buttons.register, SITE_URL)],
 
    // Кнопка 2: "Тг. канал" — відкриває основний Telegram-канал
    // Посилання формату https://t.me/channel_name Telegram відкриє всередині додатку
    [Markup.button.url(locale.buttons.channel, CHANNEL_URL)],
 
    // Кнопка 3: "Консультація" — відкриває канал або бота для консультацій
    [Markup.button.url(locale.buttons.consult, CONSULT_URL)],
 
    // Кнопка 4: "Подарунок" — єдина callback-кнопка
    //
    // callback_data: `gift:uk` або `gift:ru` залежно від locale.lang
    //
    // Чому не просто 'gift'?
    //   Якщо написати просто 'gift' — в bot.action('gift', ...) ми не знаємо,
    //   якою мовою відповісти, бо Telegram не зберігає "стан" між апдейтами.
    //   Закодовуємо мову прямо в data — і bot.action(/gift:(uk|ru)/, ...)
    //   витягне її через ctx.match[1] без жодних сесій.
    //
    // ❗ Пам'ятайте про ліміт: data не може перевищувати 64 байти.
    //   'gift:uk' = 7 байт — з великим запасом.
    //
    [Markup.button.callback(locale.buttons.gift, `gift:${locale.lang}`)],
 
  ]);
 
module.exports = { mainMenuKb };
 