// ─────────────────────────────────────────────────────────────────────────────
// utils/throttle.js — захист від повторних спрацювань кнопок
// ─────────────────────────────────────────────────────────────────────────────
//
// Проблема: Telegram надсилає апдейт на кожне натискання кнопки.
// Подвійний клік = два апдейти майже одночасно = дві повні відповіді бота.
//
// Рішення — cooldown Map:
//   Зберігаємо час останньої дії для кожної пари (userId, action).
//   Якщо новий апдейт прийшов раніше, ніж минув кулдаун — ігноруємо.
//
// Чому Map, а не просто Set заблокованих юзерів?
//   Set блокує на весь час обробки — якщо handler падає або зависає,
//   юзер буде заблокований назавжди до перезапуску бота.
//   Map з timestamp автоматично "розблоковується" після закінчення cooldownMs,
//   навіть якщо handler завершився з помилкою.
//
// Чому ключ `${userId}:${action}`, а не просто userId?
//   Роздільні кулдауни для різних дій одного юзера.
//   Наприклад: вибір мови (8 сек) і подарунок (3 сек) — незалежні.
//   Без action-частини натискання "Подарунок" блокувало б вибір мови і навпаки.
//
// ─────────────────────────────────────────────────────────────────────────────

// Єдиний Map для всього процесу — зберігається між викликами handler-ів.
// key: 'userId:action'  →  value: timestamp останнього дозволеного виклику
const cooldowns = new Map();

/**
 * Перевіряє, чи може користувач виконати дію прямо зараз.
 * Якщо так — фіксує час і повертає true.
 * Якщо ще в кулдауні — повертає false (дію слід ігнорувати).
 *
 * @param {number|undefined} userId    - ctx.from.id
 * @param {string}           action    - довільний ідентифікатор дії ('lang_select', 'gift' тощо)
 * @param {number}           cooldownMs - тривалість кулдауну в мілісекундах
 * @returns {boolean}
 */
const canProceed = (userId, action, cooldownMs) => {
  // Якщо userId відсутній (анонімний апдейт) — пропускаємо без блокування.
  // Краще обробити зайвий раз, ніж назавжди заблокувати невідомого юзера.
  if (!userId) return true;

  const key     = `${userId}:${action}`;
  const now     = Date.now();
  const lastTime = cooldowns.get(key) ?? 0; // ?? 0 — якщо ключ відсутній, вважаємо lastTime = 0

  // Кулдаун ще не минув — відхиляємо
  if (now - lastTime < cooldownMs) return false;

  // Фіксуємо час ПЕРЕД обробкою, не після.
  // Якщо поставити після — паралельні апдейти (при дуже швидких кліках)
  // пройдуть перевірку одночасно, бо обидва прочитають однаковий lastTime.
  cooldowns.set(key, now);

  // Час від часу прибираємо застарілі записи, щоб Map не росла безмежно.
  // Викликаємо не щоразу (дорого), а раз на ~200 операцій.
  if (Math.random() < 0.005) {
    pruneOldEntries(cooldownMs);
  }

  return true;
};

/**
 * Видаляє з Map записи, кулдаун яких давно минув.
 * Запобігає витоку пам'яті при великій кількості унікальних юзерів.
 *
 * @param {number} cooldownMs
 */
const pruneOldEntries = (cooldownMs) => {
  const cutoff = Date.now() - cooldownMs * 2; // видаляємо записи, старші за 2× кулдаун
  for (const [key, timestamp] of cooldowns.entries()) {
    if (timestamp < cutoff) cooldowns.delete(key);
  }
};

module.exports = { canProceed };